/**
 * ******************************************************************************
 * @brief 	: CRC16 checksum function poly:0x8005
 * @param 	  pchMessage  	: Data to check
 * @param 	  dwLength  	: Stream length
 * @param 	  wCRC  	: initialized checksum
 * @retval 	: CRC checksum
 * @author 	: chenningzhan
 * @note 	: None
 * ******************************************************************************
 */
uint16_t crc16_calc(uint8_t *pchMessage, uint32_t dwLength, uint16_t wCRC)
{
    uint8_t chData;
    if (pchMessage == NULL)
    {
        return 0xFFFF;
    }

    while (dwLength--)
    {
        chData = *pchMessage++;
        (wCRC) = ((uint16_t)(wCRC) >> 8) ^ g_CRC16_table[((uint16_t)(wCRC) ^ (uint16_t)(chData)) & 0x00ff];
    }

    return wCRC;
}

/**
 * ******************************************************************************
 * @brief 	: CRC16 Verify function
 * @param 	  pchMessage  	: Data to Verify
 * @param 	  dwLength  	: 【parameter】
 * @retval 	: True or False (CRC Verify Result)
 * @author 	: chenningzhan
 * @note 	: CRC 的验证方法也可以是看 append 后的字节流经过CRC运算后结果是否为零来判断
 * ******************************************************************************
 */
uint32_t crc16_verify(uint8_t *pchMessage, uint32_t dwLength)
{
    uint16_t wExpected = 0;

    if ((pchMessage == NULL) || (dwLength <= 2))
    {
        return __FALSE;
    }
    wExpected = crc16_calc(pchMessage, dwLength - 2, CRC16_INIT);

    return ((wExpected & 0xff) == pchMessage[dwLength - 2] && ((wExpected >> 8) & 0xff) == pchMessage[dwLength - 1]);
}

/**
 * ******************************************************************************
 * @brief 	: append CRC16 to the end of data
 * @param 	  pchMessage  	: Data to CRC and append
 * @param 	  dwLength  	: Stream length = Data + checksum
 * @author 	: chenningzhan
 * @note 	: None
 * ******************************************************************************
 */
void crc16_append(uint8_t *pchMessage, uint32_t dwLength)
{
    uint16_t wCRC = 0;

    if ((pchMessage == NULL) || (dwLength <= 2))
    {
        return;
    }
    wCRC = crc16_calc((uint8_t *)pchMessage, dwLength - 2, CRC16_INIT);

    pchMessage[dwLength - 2] = (uint8_t)(wCRC & 0x00ff);
    pchMessage[dwLength - 1] = (uint8_t)((wCRC >> 8) & 0x00ff);
}

