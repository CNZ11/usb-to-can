/**
 * ******************************************************************************
 * @brief 	: Calculate the CRC8 checksum for a given message.
 * @param 	  pchMessage  	: Pointer to the message data.
 * @param 	  dwLength  	: The length of the message data.
 * @param 	  ucCRC8  	: The initial CRC value.
 * @retval 	: The calculated CRC8 value.
 * @author 	: chenningzhan
 * @note 	: None
 * ******************************************************************************
 */
uint8_t crc8_calc(uint8_t *pchMessage, uint32_t dwLength, uint8_t ucCRC8)
{
    uint8_t ucIndex;

    while (dwLength--)
    {
        ucIndex = ucCRC8 ^ (*pchMessage++);
        ucCRC8 = g_CRC8_table[ucIndex];
    }

    return (ucCRC8);
}

/**
 * ******************************************************************************
 * @brief 	: CRC8 Verify function
 * @param 	  pchMessage  	: Data to Verify
 * @param 	  dwLength  	: The total length of the message data including the checksum.
 * @retval 	: True if the checksum is valid, False otherwise.
 * @author 	: chenningzhan
 * @note 	: CRC 的验证方法也可以是看 append 后的字节流经过CRC运算后结果是否为零来判断
 * ******************************************************************************
 */
uint32_t crc8_verify(uint8_t *pchMessage, uint32_t dwLength)
{
    uint8_t ucExpected = 0;

    if ((pchMessage == 0) || (dwLength <= 2))
        return 0;

    ucExpected = crc8_calc(pchMessage, dwLength - 1, CRC8_INIT);

    return (ucExpected == pchMessage[dwLength - 1]);
}

/**
 * ******************************************************************************
 * @brief 	: Append the CRC8 checksum to the end of a message.
 * @param 	  pchMessage  	: Data to CRC and append
 * @param 	  dwLength  	: Stream length = Data + checksum
 * @author 	: chenningzhan
 * @note 	: None
 * ******************************************************************************
 */
void crc8_append(uint8_t *pchMessage, uint32_t dwLength)
{
    uint8_t ucCRC = 0;

    if ((pchMessage == 0) || (dwLength <= 2))
        return;

    ucCRC = crc8_calc((uint8_t *)pchMessage, dwLength - 1, CRC8_INIT);

    pchMessage[dwLength - 1] = ucCRC;
}

